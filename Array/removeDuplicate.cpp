//Remove duplicate from the Sorted Array

#include<bits/stdc++.h>
using namespace std;

int removeDuplicate(int arr[],int n) { 
    // set<int> st;
    // for (int i=0;i<n; i++){
    //     st.insert(arr[i]);
    // }

    // for(int i : st) {
    //     cout << i << " ";
    // }
    // return;

    int i = 0;
    for (int j=1;j<n;j++){
        if(arr[j] != arr[i]) {
            arr[i+1] = arr[j];
            i++;
        }
    }
    
    return i+1;
}

int main() {

    int n;
    cout << "Enter size of an array: ";
    cin >> n;
 
    int arr[n];

    cout << "Enter the elements of it: ";
    for (int i=0;i<n;i++){
        cin >> arr[i];
    }

    int i = removeDuplicate(arr,n);

    for (int j=0;j<i;j++){
        cout << arr[j]  << " ";
    }
    

    return 0;
}